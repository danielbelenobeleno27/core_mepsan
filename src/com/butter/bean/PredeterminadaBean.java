package com.butter.bean;

/**
 *
 * @author usuario
 */
public class PredeterminadaBean {
    
    int cara;
    boolean precio;
    float valor;
    long familiaProducto;

    public int getCara() {
        return cara;
    }

    public void setCara(int cara) {
        this.cara = cara;
    }

    public boolean isPrecio() {
        return precio;
    }

    public void setPrecio(boolean precio) {
        this.precio = precio;
    }

    public float getValor() {
        return valor;
    }

    public void setValor(float valor) {
        this.valor = valor;
    }

    public long getFamiliaProducto() {
        return familiaProducto;
    }

    public void setFamiliaProducto(long familiaProducto) {
        this.familiaProducto = familiaProducto;
    }
    
}
